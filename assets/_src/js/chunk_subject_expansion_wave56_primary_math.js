  function findMathSubject(){
    if (typeof SUBJ === 'undefined' || !Array.isArray(SUBJ)) return null;
    return SUBJ.find(function(subj){ return subj && subj.id === 'math'; }) || null;
  }
  function findTopic(subj, id){
    if (!subj || !Array.isArray(subj.tops)) return null;
    return subj.tops.find(function(topic){ return topic && topic.id === id; }) || null;
  }
  function addTopic(subj, def){
    if (!subj || !def || !def.id || findTopic(subj, def.id)) return null;
    var topic = {
      id: def.id,
      nm: def.nm,
      dot: def.dot || subj.cl || '#2563eb',
      th: def.th || '',
      gen: function(){ return { question:'', answer:'', options:[] }; }
    };
    topic.gen = bankGen(def.rows || [], topic, subj);
    subj.tops = Array.isArray(subj.tops) ? subj.tops : [];
    subj.tops.push(topic);
    if (def.fnName) window[def.fnName] = topic.gen;
    state.addedTopics.push(def.id);
    state.totalTemplates += (def.rows || []).length;
    return topic;
  }
  function expandTopic(subj, id, rows, fnName){
    var topic = findTopic(subj, id);
    if (!topic || !(rows || []).length) return null;
    var oldGen = typeof topic.gen === 'function' ? topic.gen : (fnName && typeof window[fnName] === 'function' ? window[fnName] : null);
    var nextGen = mergeGen(oldGen, rows, topic, subj);
    topic.gen = nextGen;
    if (fnName) window[fnName] = nextGen;
    state.expandedTopics.push(id);
    state.totalTemplates += rows.length;
    return topic;
  }
  function patchGlobalMix(extraFns){
    if (typeof window.genGlobalMix !== 'function' || state.patchedGlobalMix) return;
    var base = window.genGlobalMix;
    var extras = (extraFns || []).filter(function(fn){ return typeof fn === 'function'; });
    if (!extras.length) return;
    window.genGlobalMix = function(){
      if (Math.random() < 0.72) return base();
      return pick(extras)();
    };
    state.patchedGlobalMix = true;
  }

