  function run(){
    if (![1,2,3,4].includes(grade)) return state;
    var subj = findMathSubject();
    if (!subj) return state;
    if (grade === 1) applyGrade1(subj);
    if (grade === 2) applyGrade2(subj);
    if (grade === 3) applyGrade3(subj);
    if (grade === 4) applyGrade4(subj);
    state.applied = true;
    return state;
  }

