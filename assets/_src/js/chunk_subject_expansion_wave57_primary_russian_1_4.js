  function pick(arr){ return arr[Math.floor(Math.random() * arr.length)]; }
  function shuffle(arr){
    var copy = (arr || []).slice();
    for (var i = copy.length - 1; i > 0; i -= 1){
      var j = Math.floor(Math.random() * (i + 1));
      var tmp = copy[i];
      copy[i] = copy[j];
      copy[j] = tmp;
    }
    return copy;
  }
  function uniq(values){
    var seen = Object.create(null);
    return (values || []).filter(function(value){
      var key = String(value);
      if (seen[key]) return false;
      seen[key] = true;
      return true;
    });
  }
  function makeRow(row, topic, subj){
    var options = uniq(Array.isArray(row.o) ? row.o.slice() : [row.a]);
    if (typeof mkQ === 'function'){
      return mkQ(
        row.q,
        row.a,
        shuffle(options),
        row.h || '',
        topic.nm,
        topic.dot || subj.cl || '#ea580c',
        subj.bg || '#fff7ed',
        row.code || null,
        false
      );
    }
    return {
      question: row.q,
      answer: row.a,
      options: shuffle(options),
      hint: row.h || '',
      tag: topic.nm,
      color: topic.dot || subj.cl || '#ea580c',
      bg: subj.bg || '#fff7ed'
    };
  }
  function bankGen(rows, topic, subj){
    var safeRows = (rows || []).slice();
    return function(){
      return makeRow(pick(safeRows), topic, subj);
    };
  }
  function mergeGen(oldGen, rows, topic, subj){
    var extraGen = bankGen(rows, topic, subj);
    return function(){
      if (typeof oldGen === 'function' && Math.random() < 0.45) return oldGen();
      return extraGen();
    };
  }
  function ensureTheory(key, html){
    window.RUS_TH = window.RUS_TH || {};
    if (!window.RUS_TH[key]) window.RUS_TH[key] = html;
  }
  function findRusSubject(){
    if (typeof SUBJ === 'undefined' || !Array.isArray(SUBJ)) return null;
    return SUBJ.find(function(subj){ return subj && (subj.id === 'rus' || subj.id === 'russian'); }) || null;
  }
  function findTopic(subj, id){
    if (!subj || !Array.isArray(subj.tops)) return null;
    return subj.tops.find(function(topic){ return topic && topic.id === id; }) || null;
  }
  function addTopic(subj, def){
    if (!subj || !def || !def.id || findTopic(subj, def.id)) return null;
    var topic = {
      id: def.id,
      nm: def.nm,
      dot: def.dot || subj.cl || '#ea580c',
      th: def.th || '',
      gen: function(){ return { question:'', answer:'', options:[] }; }
    };
    topic.gen = bankGen(def.rows || [], topic, subj);
    subj.tops = Array.isArray(subj.tops) ? subj.tops : [];
    subj.tops.push(topic);
    if (def.fnName) window[def.fnName] = topic.gen;
    gradeInfo.addedTopics.push(def.id);
    gradeInfo.totalTemplates += (def.rows || []).length;
    return topic;
  }
  function expandTopic(subj, id, rows, fnName){
    var topic = findTopic(subj, id);
    if (!topic || !(rows || []).length) return null;
    var replaceOnly = { capital: true };
    var oldGen = null;
    if (!replaceOnly[id]) {
      oldGen = typeof topic.gen === 'function' ? topic.gen : (fnName && typeof window[fnName] === 'function' ? window[fnName] : null);
    }
    var nextGen = oldGen ? mergeGen(oldGen, rows, topic, subj) : bankGen(rows, topic, subj);
    topic.gen = nextGen;
    if (fnName) window[fnName] = nextGen;
    gradeInfo.expandedTopics.push(id);
    gradeInfo.totalTemplates += rows.length;
    return topic;
  }
  function patchGlobalMix(extraFns){
    if (typeof window.genGlobalMix !== 'function' || gradeInfo.patchedGlobalMix) return;
    var base = window.genGlobalMix;
    var extras = (extraFns || []).filter(function(fn){ return typeof fn === 'function'; });
    if (!extras.length) return;
    window.genGlobalMix = function(){
      if (Math.random() < 0.72) return base();
      return pick(extras)();
    };
    gradeInfo.patchedGlobalMix = true;
  }
  function appendDiagnosticRows(rows){
    if (typeof QBANK === 'undefined' || !QBANK) return 0;
    var base = Array.isArray(QBANK.russian) ? QBANK.russian.slice() : [];
    var before = base.length;
    var seen = Object.create(null);
    base.forEach(function(item){
      var key = String(item.g || '') + '|' + String(item.q || '') + '|' + String(item.a || '');
      seen[key] = true;
    });
    (rows || []).forEach(function(item){
      var key = String(item.g || '') + '|' + String(item.q || '') + '|' + String(item.a || '');
      if (!seen[key]) {
        seen[key] = true;
        base.push(item);
      }
    });
    QBANK.russian = base;
    diagnosticInfo.addedRows = Math.max(0, base.length - before);
    diagnosticInfo.totalRows = base.length;
    diagnosticInfo.grades = Array.from(new Set(base.map(function(item){ return Number(item.g || 0) || 0; }).filter(Boolean))).sort(function(a,b){ return a-b; });
    return diagnosticInfo.addedRows;
  }
  function buildDiagnosticRows(){
    var rows = [];
    Object.keys(BANKS).forEach(function(key){
      var info = BANKS[key];
      var gradeNum = Number(key) || 0;
      Object.keys(info.expand || {}).forEach(function(topicId){
        var topic = findTopic(findRusSubject(), topicId);
        var topicName = topic ? topic.nm : (TOPIC_NAMES[topicId] || topicId);
        (info.expand[topicId] || []).forEach(function(item){
          rows.push({
            g: gradeNum,
            topic: topicName,
            q: item.q,
            opts: Array.isArray(item.o) ? item.o.slice() : [item.a],
            a: item.a,
            hint: item.h || 'Выбери правильный вариант по правилу русского языка.',
            src: 'wave57'
          });
        });
      });
      (info.add || []).forEach(function(def){
        (def.rows || []).forEach(function(item){
          rows.push({
            g: gradeNum,
            topic: def.nm,
            q: item.q,
            opts: Array.isArray(item.o) ? item.o.slice() : [item.a],
            a: item.a,
            hint: item.h || 'Выбери правильный вариант по правилу русского языка.',
            src: 'wave57'
          });
        });
      });
    });
    return rows;
  }
  function applyGrade(){
    if (gradeInfo.applied) return;
    if (![1,2,3,4].includes(grade)) return;
    var subj = findRusSubject();
    if (!subj) return;
    var data = BANKS[String(grade)] || null;
    if (!data) return;
    var extraFns = [];
    Object.keys(data.expand || {}).forEach(function(topicId){
      var fnMap = {
        letters:'genLetters',
        syllable:'genSyllable',
        jishi:'genJishi',
        capital:'genCapital',
        conj:'genConj'
      };
      var topic = expandTopic(subj, topicId, data.expand[topicId] || [], fnMap[topicId] || null);
      if (topic && typeof topic.gen === 'function') extraFns.push(topic.gen);
    });
    (data.add || []).forEach(function(def){
      ensureTheory(def.id, def.th || '');
      var topic = addTopic(subj, {
        id: def.id,
        nm: def.nm,
        dot: def.dot,
        th: window.RUS_TH ? window.RUS_TH[def.id] : (def.th || ''),
        rows: def.rows || [],
        fnName: def.fnName || null
      });
      if (topic && typeof topic.gen === 'function') extraFns.push(topic.gen);
    });
    patchGlobalMix(extraFns);
    gradeInfo.totalRusTopics = Array.isArray(subj.tops) ? subj.tops.length : 0;
    gradeInfo.templateTotals['grade' + grade] = gradeInfo.totalTemplates;
    gradeInfo.applied = true;
  }
  function patchDiagnostic(){
    if (diagnosticInfo.hooked) return;
    if (typeof QBANK === 'undefined') return;
    diagnosticInfo.hooked = true;
    appendDiagnosticRows(buildDiagnosticRows());
  }
  function init(){
    try { applyGrade(); } catch(_) {}
    try { patchDiagnostic(); } catch(_) {}
  }

